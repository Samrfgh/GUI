def liberties(x,y):
    lst = [[x + 1,y],[x - 1,y],[x,y + 1],[x,y - 1]]
    for i in lst:
        for j in i:
            if j == -1 or j == 19:
                lst.remove(i)
                break
    return lst

def grid_copy(grid):
    new_grid = []
    for i in grid:
        new_grid.append(i[:])
    return new_grid

